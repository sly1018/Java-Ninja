
public class SingleDimensionalArray {

	public static void main(String[] args) {

		int iarr[] = new int[3];
		iarr[0] = 1;
		iarr[1] = 2;
		iarr[2] = 3;
		System.out.println(iarr[0]);
		System.out.println(iarr[1]);
		System.out.println(iarr[2]);
		System.out.println(" ");

		for (int i = 0; i < iarr.length; i++) {
			System.out.println(iarr[i]);
		}
	}

}
